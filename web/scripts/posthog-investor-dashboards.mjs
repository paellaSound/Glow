#!/usr/bin/env node
/**
 * Creates investor-grade PostHog dashboard + insights for Glow EU project 200710.
 * Requires POSTHOG_PERSONAL_API_KEY with insight:write + dashboard:write scopes.
 *
 * Usage:
 *   POSTHOG_PERSONAL_API_KEY=phx_... node web/scripts/posthog-investor-dashboards.mjs
 */

const PROJECT_ID = process.env.POSTHOG_PROJECT_ID ?? '200710';
const API_HOST = 'https://eu.posthog.com';
const API_KEY = process.env.POSTHOG_PERSONAL_API_KEY;

if (!API_KEY) {
  console.error('Set POSTHOG_PERSONAL_API_KEY (personal API key with write access).');
  process.exit(1);
}

async function api(path, body) {
  const res = await fetch(`${API_HOST}/api/projects/${PROJECT_ID}${path}`, {
    method: 'POST',
    headers: {
      Authorization: `Bearer ${API_KEY}`,
      'Content-Type': 'application/json',
    },
    body: JSON.stringify(body),
  });

  if (!res.ok) {
    const text = await res.text();
    throw new Error(`${path} failed (${res.status}): ${text}`);
  }

  return res.json();
}

const insights = [
  {
    name: 'Activation — Sign-in to live room',
    filters: {
      insight: 'FUNNELS',
      funnel_viz_type: 'steps',
      events: [
        { id: 'signin_completed', order: 0 },
        { id: 'room_created', order: 1 },
        { id: 'device_connected', order: 2, properties: [{ key: 'device_count', value: 2, operator: 'gte' }] },
        { id: 'pattern_sent_live', order: 3 },
      ],
      date_from: '-30d',
    },
  },
  {
    name: 'Monetization — Limit hit to subscription',
    filters: {
      insight: 'FUNNELS',
      funnel_viz_type: 'steps',
      events: [
        { id: 'plan_limit_hit', order: 0 },
        { id: 'checkout_started', order: 1 },
        { id: 'checkout_completed', order: 2 },
        { id: 'subscription_activated', order: 3 },
      ],
      date_from: '-30d',
    },
  },
  {
    name: 'Retention — Rooms per team (weekly)',
    filters: {
      insight: 'TRENDS',
      events: [{ id: 'room_created', math: 'total' }],
      breakdown: 'plan_code',
      breakdown_type: 'event',
      date_from: '-90d',
      interval: 'week',
    },
  },
  {
    name: 'Scale — Peak devices by plan tier',
    filters: {
      insight: 'TRENDS',
      events: [
        {
          id: 'device_connected',
          math: 'avg',
          math_property: 'peak_device_count',
        },
      ],
      breakdown: 'plan_code',
      breakdown_type: 'event',
      date_from: '-30d',
      interval: 'day',
    },
  },
  {
    name: 'Acquisition — Sign-ins by method',
    filters: {
      insight: 'TRENDS',
      events: [{ id: 'signin_completed', math: 'total' }],
      breakdown: 'method',
      breakdown_type: 'event',
      date_from: '-30d',
      interval: 'day',
    },
  },
  {
    name: 'Reliability — Exceptions by surface',
    filters: {
      insight: 'TRENDS',
      events: [{ id: '$exception', math: 'total' }],
      breakdown: 'surface',
      breakdown_type: 'event',
      date_from: '-7d',
      interval: 'day',
    },
  },
];

async function main() {
  const createdInsightIds = [];

  for (const spec of insights) {
    const result = await api('/insights/', {
      name: spec.name,
      filters: spec.filters,
    });
    createdInsightIds.push(result.id);
    console.log(`Created insight: ${spec.name} (${result.id})`);
  }

  const dashboard = await api('/dashboards/', {
    name: 'Glow — Investor Pack',
    description:
      'Activation, monetization, retention, and scale metrics for investor reporting. Auto-generated by posthog-investor-dashboards.mjs',
    tiles: createdInsightIds.map((id) => ({ insight: id })),
  });

  console.log(`\nDashboard ready: ${API_HOST}/project/${PROJECT_ID}/dashboard/${dashboard.id}`);
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
